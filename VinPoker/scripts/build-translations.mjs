function flatten(obj, prefix="") {
  const r = {};
  for (const [k, v] of Object.entries(obj)) {
    const key = prefix ? prefix + "." + k : k;
    if (typeof v === "string") r[key] = v;
    else if (v && typeof v === "object") Object.assign(r, flatten(v, key));
  }
  return r;
}

function unflatten(flat) {
  const r = {};
  for (const [key, val] of Object.entries(flat)) {
    const parts = key.split(".");
    let cur = r;
    for (let i = 0; i < parts.length - 1; i++) {
      if (!cur[parts[i]]) cur[parts[i]] = {};
      cur = cur[parts[i]];
    }
    cur[parts[parts.length - 1]] = val;
  }
  return r;
}

function translateLocale(langCode, langMap, label) {
  const fp = join(LOCALES_DIR, `${langCode}.json`);
  const data = JSON.parse(readFileSync(fp, "utf8"));
  const flat = flatten(data);

  let translated = 0;
  let skipped = 0;
  const prefix = `[${label}] `;

  for (const [key, val] of Object.entries(flat)) {
    if (val.startsWith(prefix)) {
      const engValue = val.slice(prefix.length);
      if (langMap.has(engValue)) {
        flat[key] = langMap.get(engValue);
        translated++;
      } else {
        // Fallback to clean English
        flat[key] = engValue;
        skipped++;
      }
    }
  }

  const out = unflatten(flat);
  writeFileSync(fp, JSON.stringify(out, null, 2) + "\n", "utf8");
  console.log(`${langCode}.json: ${translated} translated, ${skipped} fallback to English`);
}

